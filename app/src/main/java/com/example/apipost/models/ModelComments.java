package com.example.apipost.models;

public class ModelComments {


    private int postId;
    private int id;
    private String name;
    private String email;
    private String body;

    public Integer getPostId() {
        return postId;
    }

    public Integer getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getBody() {
        return body;
    }
}
